public class Student {
    private String id;
    private String surName;
    private String name;
    private String patronymic;
    private int birthDate;
    private String address;
    private String phoneNumber;
    private String faculty;
    private String course;
    private String group;

    public Student(String id, String surName, String name, String patronymic, int birthDate,
                   String address, String phoneNumber, String faculty, String course, String group) {
        this.id = id;
        this.surName = surName;
        this.name = name;
        this.patronymic = patronymic;
        this.birthDate = birthDate;
        this.address = address;
        this.phoneNumber = phoneNumber;
        this.faculty = faculty;
        this.course = course;
        this.group = group;
    }

    public Student(String id, String surName, String name) {
        this.id = id;
        this.surName = surName;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSurName() {
        return surName;
    }

    public void setSurName(String surName) {
        this.surName = surName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPatronymic() {
        return patronymic;
    }

    public void setPatronymic(String patronymic) {
        this.patronymic = patronymic;
    }

    public int getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(int birthDate) {
        this.birthDate = birthDate;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getFaculty() {
        return faculty;
    }

    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    @Override
    public String toString() {
        return "id " + id +
                " Sur Name " + surName +
                " Name " + name +
                " Patronymic " + patronymic +
                " Birth Date " + birthDate +
                " Address " + address +
                " Phone Number " + phoneNumber +
                " Faculty " + faculty +
                " Cource " + course +
                " Group " + group;
    }
}
